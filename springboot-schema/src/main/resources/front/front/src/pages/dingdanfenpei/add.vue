<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				dingdanbianhao : false,
				zhanghao : false,
				xingming : false,
				shouji : false,
				touxiang : false,
				mudedi : false,
				fenpeishijian : false,
				beizhu : false,
				sijizhanghao : false,
				sijixingming : false,
				chepaihao : false,
				lianxidianhua : false,
				luxianmingcheng : false,
				jiage : false,
				sfsh : false,
				shhf : false,
				userid : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          dingdanbianhao: '',
          zhanghao: '',
          xingming: '',
          shouji: '',
          touxiang: '',
          mudedi: '',
          fenpeishijian: '',
          beizhu: '',
          sijizhanghao: '',
          sijixingming: '',
          chepaihao: '',
          lianxidianhua: '',
          luxianmingcheng: '',
          jiage: '',
          userid: '',
        },
        sijizhanghaoOptions: [],
        luxianmingchengOptions: [],


        rules: {
          dingdanbianhao: [
          ],
          zhanghao: [
            { required: true, message: '账号不能为空', trigger: 'blur' },
          ],
          xingming: [
            { required: true, message: '姓名不能为空', trigger: 'blur' },
          ],
          shouji: [
            { required: true, message: '手机不能为空', trigger: 'blur' },
          ],
          touxiang: [
          ],
          mudedi: [
          ],
          fenpeishijian: [
          ],
          beizhu: [
          ],
          sijizhanghao: [
          ],
          sijixingming: [
          ],
          chepaihao: [
          ],
          lianxidianhua: [
          ],
          luxianmingcheng: [
          ],
          jiage: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          sfsh: [
          ],
          shhf: [
          ],
          userid: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.fenpeishijian = this.getCurDateTime()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='dingdanbianhao'){
              this.ruleForm.dingdanbianhao = obj[o];
              this.ro.dingdanbianhao = true;
              continue;
            }
            if(o=='zhanghao'){
              this.ruleForm.zhanghao = obj[o];
              this.ro.zhanghao = true;
              continue;
            }
            if(o=='xingming'){
              this.ruleForm.xingming = obj[o];
              this.ro.xingming = true;
              continue;
            }
            if(o=='shouji'){
              this.ruleForm.shouji = obj[o];
              this.ro.shouji = true;
              continue;
            }
            if(o=='touxiang'){
              this.ruleForm.touxiang = obj[o].split(",")[0];
              this.ro.touxiang = true;
              continue;
            }
            if(o=='mudedi'){
              this.ruleForm.mudedi = obj[o];
              this.ro.mudedi = true;
              continue;
            }
            if(o=='fenpeishijian'){
              this.ruleForm.fenpeishijian = obj[o];
              this.ro.fenpeishijian = true;
              continue;
            }
            if(o=='beizhu'){
              this.ruleForm.beizhu = obj[o];
              this.ro.beizhu = true;
              continue;
            }
            if(o=='sijizhanghao'){
              this.ruleForm.sijizhanghao = obj[o];
              this.ro.sijizhanghao = true;
              continue;
            }
            if(o=='sijixingming'){
              this.ruleForm.sijixingming = obj[o];
              this.ro.sijixingming = true;
              continue;
            }
            if(o=='chepaihao'){
              this.ruleForm.chepaihao = obj[o];
              this.ro.chepaihao = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='luxianmingcheng'){
              this.ruleForm.luxianmingcheng = obj[o];
              this.ro.luxianmingcheng = true;
              continue;
            }
            if(o=='jiage'){
              this.ruleForm.jiage = obj[o];
              this.ro.jiage = true;
              continue;
            }
            if(o=='userid'){
              this.ruleForm.userid = obj[o];
              this.ro.userid = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.sijizhanghao!=''&&json.sijizhanghao) || json.sijizhanghao==0){
                this.ruleForm.sijizhanghao = json.sijizhanghao            }
            if((json.sijixingming!=''&&json.sijixingming) || json.sijixingming==0){
                this.ruleForm.sijixingming = json.sijixingming            }
            if((json.chepaihao!=''&&json.chepaihao) || json.chepaihao==0){
                this.ruleForm.chepaihao = json.chepaihao            }
            if((json.lianxidianhua!=''&&json.lianxidianhua) || json.lianxidianhua==0){
                this.ruleForm.lianxidianhua = json.lianxidianhua            }
          }
        });
        this.$http.get('option/siji/sijizhanghao', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.sijizhanghaoOptions = res.data.data;
          }
        });
        this.$http.get('option/luxianguihua/luxianmingcheng', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.luxianmingchengOptions = res.data.data;
          }
        });

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },
      // 下二随
      sijizhanghaoChange () {
        this.$http.get('follow/siji/sijizhanghao?columnValue=' + this.ruleForm.sijizhanghao, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.sijixingming){
              this.ruleForm.sijixingming = res.data.data.sijixingming
            }
            if(res.data.data.chepaihao){
              this.ruleForm.chepaihao = res.data.data.chepaihao
            }
            if(res.data.data.lianxidianhua){
              this.ruleForm.lianxidianhua = res.data.data.lianxidianhua
            }
            if(res.data.data.jiage){
              this.ruleForm.jiage = res.data.data.jiage
            }
          }
        });
      },
      // 下二随
      luxianmingchengChange () {
        this.$http.get('follow/luxianguihua/luxianmingcheng?columnValue=' + this.ruleForm.luxianmingcheng, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            if(res.data.data.sijixingming){
              this.ruleForm.sijixingming = res.data.data.sijixingming
            }
            if(res.data.data.chepaihao){
              this.ruleForm.chepaihao = res.data.data.chepaihao
            }
            if(res.data.data.lianxidianhua){
              this.ruleForm.lianxidianhua = res.data.data.lianxidianhua
            }
            if(res.data.data.jiage){
              this.ruleForm.jiage = res.data.data.jiage
            }
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`dingdanfenpei/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('dingdanfenpei/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`dingdanfenpei/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`dingdanfenpei/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      touxiangUploadChange(fileUrls) {
          this.ruleForm.touxiang = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");
      },
    }
  };
</script>

